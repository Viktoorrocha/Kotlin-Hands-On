fun main(args: Array<String>){
    var poligono = "Hexagono"
    println("Este poligono é um $poligono")
    poligono = ("Octógono")
    println("Agora este poligono é um $poligono")
}

/*
Este poligono é um Hexagono
Agora este poligono é um Octógono
*/


/*
Só podemos alterar o valor de variáveis declaradas com " VAR " 

Se a palavra reservada VAL for utilizada, esta variável será tratada como uma constante. 
